# Variable Demo

# String variable
name = "Python"
print(f"Name: {name}")

# Integer variable
age = 25
print(f"Age: {age}")

# Float variable
height = 5.9
print(f"Height: {height}")

# Boolean variable
is_active = True
print(f"Is Active: {is_active}")

# List variable
colors = ["red", "green", "blue"]
print(f"Colors: {colors}")

# Dictionary variable
person = {"name": "John", "age": 30, "city": "New York"}
print(f"Person: {person}")
